package javacourse;

public class Assignment {
	
	    public static void main(String[] args) {
	        int a = 10;  //(Simple Assignment operator)
	        
	        a += 5;  // Equivalent to a = a + 5  ( Addition Assignment )
	        System.out.println("a += 5: " + a); // Output: a += 5: 15
	        
	        a -= 3;  // Equivalent to a = a - 3  ( Subtraction Assignment )
	        System.out.println("a -= 3: " + a); // Output: a -= 3: 12
	        
	        a *= 2;  // Equivalent to a = a * 2  ( Multiplication Assignment )
	        System.out.println("a *= 2: " + a); // Output: a *= 2: 24
	        
	        a /= 4;  // Equivalent to a = a / 4  ( Division Assignment )
	        System.out.println("a /= 4: " + a); // Output: a /= 4: 6
	        
	        a %= 3;  // Equivalent to a = a % 3  ( Modulus Assignment )
	        System.out.println("a %= 3: " + a); // Output: a %= 3: 0
	    }
	}


